<h1> 			<img src="http://www.syrnia.com/images/skills/woodcutting.gif" alt="" /> Woodcutting 		</h1>
Simply put, this is the skill of chopping down trees. The wood received can be used to build houses, boats and shops (see constructing). Also if you wish to cook you will need wood. As your woodcutting level increases you will be able to use better hatchets. Sometimes you may get more than 1 wood from a chop, but the experience remains the same. Also while chopping wood you may get seeds which can be used to farm (see farming) or a chest from inside a hollow tree. The chest can contain items such as gems or seeds. To cut some wood, equip your hatchet, and select the woodcut option at an appropriate location. <br />
You can start woodcutting at 'Isri'.<br />
<br />
You need wood for a variety of skills. It can be used for <a href="http://www.syrnia.com/index.php?page=manual&amp;m=smithing">Smithing</a> new tools, <a href="http://www.syrnia.com/index.php?page=manual&amp;m=constructing">Construction</a> of houses, shops or boats, or for <a href="http://www.syrnia.com/index.php?page=manual&amp;m=cooking">Cooking</a> food. You may also find seeds from woodcutting. These can be used in <a href="http://www.syrnia.com/index.php?page=manual&amp;m=farming">Farming</a>.<br />
