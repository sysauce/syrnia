<strong><font size="4">Want to mine ? </font><br />
Collect ore to sell or to smith and gain mining levels.<br />
<br />
</strong>Alright, you started with an pickaxe, so we got everything we need to mine.<br />
<ol>
    <li>Look at the MAP* and click on Rynir mines, this location has got ore which we'll be mining.</li>
    <li>After clicking you will be moving, this will take some seconds.(Any action is displayed in the MAIN WINDOW*)</li>
    <li>When the timer has counted down to 0 you will be in Rynir mines, the MAP* has changed and the town title is displayed at the LOCATION NAME*</li>
    <li><img alt="Bronze pickaxe" src="images/inventory/Bronze pickaxe.gif" />Equip your Pickaxe by clicking on it in the INVENTORY*, after clicking the pickaxe will be displayed below GAME MENU at your equipment status.</li>
    <li>Click on 'Mine copper ore' at the CITY MENU*</li>
    <li>You are now mining (Displayed in the MAIN WINDOW*) and every time the counter reaches 0 you will gain one Copper ore, the copper ore will be added to your INVENTORY* after a while. The amount of ore is also displayed up to date in the MAIN WINDOW*.</li>
</ol>
If you got At least 10 copper and 10 tin ore you could begin Smithing.<br />
<br />
<u>To smith:<br />
</u>
<ol>
    <li>Move to Endarx (Rynir mines to Lisim, From Lisim to Valera, from Valera to Endarx)</li>
    <li>Click on 'Smelt bronze bars'</li>
    <li>Wait until you have no more ore left to smelt</li>
    <li>Equip your hammer <img alt="" src="images/inventory/Hammer.gif" /> by clicking on it in the INVENTORY*</li>
    <li>Click on 'Smith' at the CITY MENU*</li>
    <li>You can now select something to smith, like an Bronze dagger. When the smithing starts you can wait until you run out of bars.</li>
</ol>
<br />
<strong><font size="4">Want to Fish ? </font><br />
Collect fish to sell or to cook and gain fishing levels.<br />
<br />
</strong>Alright, you started with an net, so we got everything we need to begin fishing<br />
<ol>
    <li>Look at the MAP* and click on Lisim, this location is famous for it's unlimited supply of shrimps which we'll be fishing.</li>
    <li>After clicking you will be moving, this will take some seconds.(Any action is displayed in the MAIN WINDOW*)</li>
    <li>When the timer has counted down to 0 you will be in Lisim, the MAP* has changed and the town title is displayed at the LOCATION NAME*</li>
    <li><img alt="" src="images/inventory/Net.gif" />Equip your net by clicking on it in the INVENTORY*, after clicking the net will be displayed below GAME MENU at your equipment status.</li>
    <li>Click on 'Fish' at the CITY MENU*</li>
    <li>You are now fishing (Displayed in the MAIN WINDOW*) and every time the counter reaches 0 you will gain one Shrimp, the shrimp will be added to your INVENTORY* after a while. The amount of fish is also displayed up to date in the MAIN WINDOW*.</li>
</ol>
If you got At least 10 shrimps you could go to Harith and use your tinderbox to cook:<br />
<br />
<ol>
    <li>Equip your tinderbox</li>
    <li>Move to Harirth (From Lisim to Rynir Mines, from Rynir mines to Harith)</li>
    <li>Click on &quot;Cook shrimps&quot;</li>
</ol>
