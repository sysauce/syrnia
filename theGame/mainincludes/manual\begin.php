<h1>Getting Started in Syrnia 		</h1>
Hi and welcome to Syrnia! This guide will not explain everything, but will tell you how to start playing right away, if you <u>do</u> want to learn about Syrnia features, please begin reading the Advanced beginners manual. This manual is written for people who hate reading manuals ;).<br />
After logging in you should first complete the Beginner tutorial, when you've completed it and are at Sanfew you could use this manual for your next steps.<br />
