<h1> <img alt="" src="http://www.syrnia.com/images/skills/cooking.gif" /> Cooking </h1>
Hungry? Then cook yourself a lovely fish meal! All you need is some <em>fish, wood,</em> and a <em>tinderbox</em>. Equip the <em>tinderbox</em> and you are ready to cook. Then on the City Menu, there will be options to cook all the types of raw fish you currently have in your inventory. Make sure you have a Tinderbox, Fish, and Wood. You can get these by <a href="http://www.syrnia.com/index.php?page=manual&amp;m=fishing">Fishing</a> and <a href="http://www.syrnia.com/index.php?page=manual&amp;m=woodcutting">Woodcutting</a> at various locations. The first place to start your cooking adventure is <strong>Harith</strong> on Remer Island. When the counter reaches zero, the wood and fish are removed from your inventory and you will get back either a cooked fish or a burned fish. If it is cooked, you can then eat it to regain health. You also gain full EXP for it. Cooked fish you get from this skill can heal you in <a href="http://www.syrnia.com/index.php?page=manual&amp;m=combat">Combat</a>. A burned fish cannot be eaten and has no real purpose. You also receive 1 EXP for your efforts. Generally, the higher your level, the more chance of success.<br />
