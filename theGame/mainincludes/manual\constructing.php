<h1> 			<img alt="" src="http://www.syrnia.com/images/skills/constructing.gif" /> Constructing 		</h1>
From shops to ships, there are many things to build in Syrnia. Constructing is  		the skill you need to take advantage of building shops, boats and houses. Like  		the Trading skill, Constructing is a skill which you can study for. But unlike the trading skill, you can gain experience in other ways, such as building houses or shops. You also need Construction to build fishing boats and catch bigger fish (see <a href="http://www.syrnia.com/index.php?page=manual&amp;m=fishing">Fishing</a>). Wood is a key part of this skill, so you may need a lot of it (see <a href="http://www.syrnia.com/index.php?page=manual&amp;m=woodcutting">Woodcutting</a>). It is the only building resource for all of Syrnia's shops, houses and boats. You also need a hammer to build things. Money is another key resource. You may also need to study, to further boost your levels. This can be very expensive.<br />
<br />
