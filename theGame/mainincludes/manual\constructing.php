<h3> How to build </h3>
To build something, first equip a hammer and click once on the city name in the city menu (to refresh the screen). Click the &quot;construct&quot; above &quot;player shops&quot; and then in the center work screen select the item you would like to construct. Remember you can only have one house or shop in each location, but once built you do have the option to upgrade with only an additional cost in wood. You will need different amounts of wood to build different things with bigger buildings needing more wood. For more details, see the table below.<br />
<h3><br />
How to Demolish</h3>
If you aren't happy, you can demolish your house or shop. On the options for your home or shop, click the option saying &quot;Destroy my house&quot; or &quot;Destroy the shop&quot;. It will then ask you to confirm your choice at the top of the menu. Once confirmed, the building will be destroyed instantly and you will get back some of the wood you used to build it. Beware, if you have any items or gold in your home or shop and you then decide to destroy it, the items and gold will be lost.<br />
<h3>How to Upgrade</h3>
Houses and shops can also be upgraded in size. This requires no construction level to upgrade but it does require significantly more wood than if it were built from nothing. This is because you would have to spend a lot of money paying for construction levels to build a house of that size, but upgrading requires no extra levels and so creates balance. You can however, achieve sizes of homes not noted in the list. There is a limit though onto how much it can be upgraded. Houses are around the 10,000 slot size.<br />
<h3><br />
Houses</h3>
Houses are storage places for items. Items are stored in &quot;slots&quot;, with one item per slot. Remember that several of one type of item do not count as one and for example10 logs of wood require 10 slots in a house. Every item though, takes up just one slot each, no matter how big. Bigger houses can hold more items and require much more wood to build them.<br />
