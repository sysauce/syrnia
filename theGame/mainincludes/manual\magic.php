<h1><img alt="" src="http://www.syrnia.com/images/skills/magic.gif" /> Magic </h1>
<p>Cast spells, and perform many acts of wizardry and skill! To perform magic, you will need to begin with the Bluebell Quest. If you have not completed the Bluebell Quests, then you cannot perform magic. Every magician also needs a staff, otherwise how will you cast those spells? <br />
<br />
To perform magic, you need to go to Bluebell. Select <em>enchant</em> from the menu, and then select the item to enchant. Simple as that! <br />
<br />
Spells can vary in effectiveness and the skills they affect. You will also need <em>Gems/Stones</em> to perform the spells, as well as other ingredients found elsewhere in the world of Syrnia. </p>
<p>Bluebell can only go so far though. She does not yet know how to make the mysterious orbs. You must go to other, more powerful mages to learn these skills.</p>
<p>For all magic though, you need gems. Gems can be found in many ways. They can be dropped by monsters in combat, they could be found inside boxes, or they could also be dug out of the ground while mining. (See <a href="http://www.syrnia.com/index.php?page=manual&amp;m=mining">Mining</a> for more information).<br />
</p>
<p> </p>
<h3>Advanced Magic:</h3>
If you have level 20 Magic, you can also perform Magic with the powerful White Wizard of Kanzo island. To get to him though, you will need to use some magic of your own and get a Kanzo Teleport Orb to transport you there. You would also need a Penteza Telport Orb to leave this mysterious island.<br />
<br />
In Penteza, there is another mage, who will also teach spells to the high levelled player. You will need Level 20 to train with him as well. They will allow you to create teleport orbs.&nbsp;<br />
