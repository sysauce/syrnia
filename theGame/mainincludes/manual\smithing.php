<h1><img alt="" src="http://www.syrnia.com/images/skills/smithing.gif" /> Smithing </h1>
Smithing is a skill used to smelt ores (from mining) into bars, and working with the bars and a <em>hammer</em> to create various items of armour, weapons and tools. When you wish to smelt, go to an appropriate location and select the <em>smelt bars</em> option for the bars you wish to make. To smith, equip your <em>hammer</em> and select the <em>smith</em> option. You will be given a choice of metals to work with. Providing you have the correct bars and necessary smithing level, you will be given a list of things to make. Select one of these and you will make that item providing you have the sufficient materials. You will continue to make this item until you select a new item to make, run out of materials, or stop smithing. Below you can find information on smelting, smithing, and where to smelt/smith. <br />
<br />
For smelting bars of metal, you need ore. You can get this from <a href="http://www.syrnia.com/index.php?page=manual&amp;m=mining">Mining</a>. When Smithing the bars, you may also need to do some <a href="http://www.syrnia.com/index.php?page=manual&amp;m=woodcutting">Woodcutting</a> and get some wood to make tools.<br />
<br />
The tools made from this skill can be used to woodcut, mine or even used in <a href="http://www.syrnia.com/index.php?page=manual&amp;m=combat">Combat</a>.<br />
