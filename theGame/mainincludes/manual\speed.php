<h1><img src="http://www.syrnia.com/images/skills/speed.gif" alt="" /> Speed/Travel </h1>
Nothing quite beats a good stroll in the countryside of Syrnia. In order to get around Syrnia, a player must move between locations. Walking takes time, and this time can be affected by several things. If you are using a horse or a mule, your journey will be a lot quicker but you gain no experience from it. Using Leather Boots or Eagle Boots reduce travel time by a few seconds, so to does the affect of Travel Time enchantments (See <a href="http://www.syrnia.com/index.php?page=manual&amp;m=magic">Magic</a>).<br />
<br />
<h3>Animals</h3>
Using animals can get you to a place a lot faster. Each one takes a large amount of time from your journey, with better animals taking even more time off your route. Using animals though, has a slight penalty, you do not gain any Speed Experience for your journey and you will not find flowers. Also, animals have a slight and random chance of dying. If it dies it is removed from your inventory and you will then have to walk.<br />
