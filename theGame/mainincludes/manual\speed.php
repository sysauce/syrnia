<h3>Sailing</h3>
This allows you to travel from island to island. There is only one port on an island, this is your gateway to other lands. From each port you can only sail to certain locations. You may have to go from that new port to another one to reach your destination. There is a fee for using a boat there, however if you have your own boat, travel is free. The only exception is Heerchey Island, which to get there, you must pay 500 gold. Travel back from most islands is usually free, however Exrollia is not. Make sure you dont get stranded on there!<br />
