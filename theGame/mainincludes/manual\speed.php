<h3>Flowers</h3>
<br />
While walking from place to place, you may find flowers. You will not find them while using a horse though. When you reach your destination, you might have picked them up on the way. The chances of finding flowers increase as your Speed level increases. These flowers can then be sold to the flower shops at Penteza or Elven Gate.<br />
<br />
Some flowers can only be found at special locations.<br />
