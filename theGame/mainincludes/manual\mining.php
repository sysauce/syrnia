<h1> 			<img src="http://www.syrnia.com/images/skills/mining.gif" alt="" /> Mining 		</h1>
Mining is the art of obtaining ore from rocks found in the various mines of  		Syrnia. Mining requires that you have a <em>pickaxe</em> in your posession. Once you have equipped your axe, you can go to the mines and start work. The first place you will encounter outside of Tutorial Island, is Rynir Mines. Mining is an essential skill and the ores you can get from here are used in the <a href="http://www.syrnia.com/index.php?page=manual&amp;m=smithing">Smithing</a> process to make new tools and equipment.<br />
