<h1> 			<img src="http://www.syrnia.com/images/skills/thieving.gif" alt="" /> Thieving 		</h1>
Put simply, Thieving is the art of stealing without being noticed. A good thief  		will gain gold from other players, and not get sent to jail that often!  		Speaking of jails, every island has got its own jail.<br />
<h3>&nbsp;</h3>
<h3>Player Thieving<br />
</h3>
<br />
Player thieving is simple to undertake but is also rather difficult to succeed in. Choose a player who is at the same location as yourself and search for their name in the &quot;Players at this location&quot; list. Select their name and below the player's stats will be a link to &quot;Thief player&quot;. Click the link and you will have a go. The result will be seen instantly. If you are successful, you will be notified that you have stolen money from them. The amount you receive is random, but the more money the player has on them, the more money you are likely to get. The target is not told about the attempt and you are also given an option to try again. For a successful thieving attempt you earn 20 EXP.<br />
<br />
If you fail to steal from them however, you will be captured and put into jail. The player is told that you have attempted to steal from them. Jail time varies but is decided by your thieving level compared to your target. If you have a higher level than your target, you will get more time in jail. The lowest time available though is 150 seconds (2 minutes 30 seconds). Even if you fail though, you will still get 5 EXP for trying.<br />
<br />
<h3>Shop Thieving</h3>
At level 10, players can attempt to steal items from shops. Go to a shop and next to every item is an option to steal the item. The higher the price, the lower the chances of being successful. There is always a chance of being successful no matter what the price, however the chances might be very low. If you are successful, you will take the item and have an option to go back to the shop. Like player thieving, the owner of the shop is not told if you have been successful. If you fail to steal the item though, you will be caught and thrown in jail. The player will be told of your attempt and your time in jail depends on what you tried to steal. For every gold piece the item was worth, your time in jail increases by 25 seconds. So for example, a 10 Gold pieces item would result in a jail time of 250 seconds.<br />
