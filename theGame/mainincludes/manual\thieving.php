<h3>Special Events</h3>
Every so often, a mysterious crate will appear somewhere. These boxes are combination locked and the combination is usually within a certain range. Players usually get one guess every 20 seconds. You can guess as many times as you like with every failed attempt getting you some experience for trying. The amount of experience you will get depends on how many possible numbers there are. More numbers available means more experience. If you open the box though with the correct combination, you shall receive whatever mysteries lie within...
