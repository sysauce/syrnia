You can also attempt to steal gold from a shop. This is more risky but the rewards can be much greater. On the shop window, click the button at the top to steal gold. If you are successful, you will take gold that the shop is currently storing and gain 30 EXP. The owner is not notified of your attempt. Failure however, results in a fixed 30 minute jail time. The player will also be warned and you do gain 5 EXP for trying though.<br />
<br />
<br />
<h3> 			The Thief Guild 		</h3>
The <em>Thief Guild</em> is located on Mezno Island at the town of Mentan. You  		will need to do the thieving trail quest before you can enter however. When you  		are a member of the thieving guild you will be able to do special theiving jobs  		that will gain you gold as well as thieving experience (exp). For these jobs, you are usually given a time limit and a target. You will be told their last known location and you will need to thieve them to scare them. You must be swift, find your target and strike. Success or failure in your attempt does not matter. You will have scared them enough to satisfy the guild. Then make your return to the guild in your own time to collect your reward. The quicker you strike, the greater your reward.<br />
<br />
<br />
<h3> 			Locked Chests</h3>
Another good way to practice thieving is picking locks, however you need locked chests which are hard to get. Using a lock pick you will be able to open locked chests. Simply equip the lock pick and then select the box you wish to unlock. Wait for the timer to reach 0, and the box is unlocked and ready to open.<br />
