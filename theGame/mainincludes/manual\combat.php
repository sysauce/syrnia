<h3>Strength </h3>
Strength increases your maximum damage. The higher this is, the more you have the potential to inflict. This usually goes with the &quot;Attack&quot; skill for best effect.<br />
<br />
<h3>Health </h3>
The more health you have, the longer you will be able to survive in battle. Your health <img src="http://www.syrnia.com/images/heart.gif" alt="" /> can be replenished with food.
<h3>Level </h3>
Your level <img src="http://www.syrnia.com/images/level.gif" alt="" /> is a global representation of how much of a warrior you are. Use it as a measurement of how you compare with certain creatures/enemies. For example, if your level is 4, don't go fighting a level 10 creature otherwise you may just get killed! Speaking of which, you will end up in Sanfew, minus anything you were carrying if you die. If you are a pirate and die you will return to Crab Nest not Sanfew.
