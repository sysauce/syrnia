<h1><img src="http://www.syrnia.com/images/skills/attack.gif" alt="" /> Combat </h1>
Combat can be split into 4 categories - Attack, Defence, Strength and Health. You will notice that on your screen to the left there is a drop-down list box with these options in it. The one you select is the aspect of your combat skills that will be worked on when you train and fight. As with the other skills you can select the option to train/fight from the city menu of an appropriate location. <br />
<br />
Before you practice in combat, it is recommended that you have armour and weaponry (see <a href="http://www.syrnia.com/index.php?page=manual&amp;m=smithing">Smithing</a>), and also some food to heal yourself with (see <a href="http://www.syrnia.com/index.php?page=manual&amp;m=farming">Farming</a> and <a href="http://www.syrnia.com/index.php?page=manual&amp;m=cooking">Cooking</a>). It is also advised that you have a house to store items (see <a href="http://www.syrnia.com/index.php?page=manual&amp;m=constructing">Construction</a>).<br />
<br />
Details of the 4 combat skills are given below as well as details on where you can fight/train: <br />
