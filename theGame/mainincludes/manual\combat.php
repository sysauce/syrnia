<h3>Battle:</h3>
When you enter a battle, you will be shown the battle window. The timer will start counting down. At the end of the timer, both you and your opponent will exchange hits. You may do damage and also take damage. You may also miss or not be hit at all. This is called a &quot;Round&quot;. After each round, you have the chance to take items of food to recover lost health. The battle continues and both Player and monster go through Rounds until one of them is defeated. <br />
<br />
If you win the fight, you will gain combat experience in whichever skill you have chosen to train using the Menu on the left of the screen. You may also pick up items that the monster has dropped. However if you are not so lucky and the monster kills you, all inventory items will be dropped on the ground where you fell, for other players to potentially take, and will be brought back to life at Sanfew, or if you are a pirate, Crabs Nest.
<h3>&nbsp;</h3>
Outlands Combat is different. There is a large grey window in the centre of the screen. This shows all previous battles at that location. To the right of this, is a list of any players at this location. It will always show yourself at that location, however if someone else is at the same Outlands location as you, their name will be displayed. This will show their name, their Combat Level and Health Points. You then have the option to Attack them using the &quot;ATT&quot; button. You will then strike at that player. The defender will automatically retaliate. There is no timer, it is all about clicking the &quot;ATT&quot; button. If you need to heal, you must go through your inventory and eat food from there, so make sure you have an organised inventory. If a player kills another player, the loser is sent back to Sanfew or Crabs Nest, lose all their items at that Outlands location and will usually be sent a message telling them who killed them and at which location.<br />
