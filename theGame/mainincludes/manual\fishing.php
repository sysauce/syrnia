<h1> 			<img src="http://www.syrnia.com/images/skills/fishing.gif" alt="" /> Fishing 		</h1>
Syrnia is ripe with many varying fish and associated sub-water  		species. Fishing is available at many locations across Syrnia. At first though, you are limited to fishing using a Net. It is recommended to start at Lisim. At level 5, you can then start to use a fishing rod and expand the number of fish you can catch. Eventually you can have fishing boats constructed (See <a href="http://www.syrnia.com/index.php?page=manual&amp;m=constructing">Construction</a>) and set out into the open seas for even bigger fish. The fish you gather can be cooked for food (See <a href="http://www.syrnia.com/index.php?page=manual&amp;m=cooking">Cooking</a>), and then this can be used to recover lost health in combat (See <a href="http://www.syrnia.com/index.php?page=manual&amp;m=combat">Combat</a>). <br />
