<h4>Cave of Trades</h4>
<p>There is another method of item sale available in Syrnia. This is known as the Cave of Trades. It was founded by Ogre Trendus. Players can go to these places and they can auction their items. All Cave of Trades on the map work in exactly the same way and will all offer the exact same selection of auctions and offers. Players will decide what item(s) they wish to put up for sale, how many of that item and then for how long they wish to leave the auction open. An auction can be left for either six hours, 12 hours, 24 hours or 48 hours. Once the item has been put up for auction, it cannot be taken down. <br />
</p>
<p><br />
During that time however, players can bid for that item. They must bid at least 1% more than the current highest bid to take control and have the top offer. At any time though, someone could go into the Cave of Trades and place a bid on that item that is higher than your bid. If that happens, they spend their gold to take control and hold the highest bid and the player who originally had control is given a message informing them that they have lost control and returns their money. At the end of the time, whoever holds the highest bid will win the item, which is then delivered to their inventory. If however, no bids were taken on an item at all, you receive a notice that it did not sell and you must return to the Cave of Trades to collect it. You can have auctions running while bidding on others, (but you cannot bid on your own lots!).</p>
