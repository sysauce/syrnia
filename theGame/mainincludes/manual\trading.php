<h3>Player Shops</h3>
Players can also use shops to sell goods to others. First you need to build a shop, this requires <a href="http://www.syrnia.com/index.php?page=manual&amp;m=constructing">Construction</a> and also plenty of wood to build with. You need to make sure that you have a high enouhg trading level, as your trading level controls how many shop slots you can run. If you can only support 20 shops in a 50 slot size of shop for example, then you can only use those 20. The other 30 are unavailable.<br />
<br />
Once you have built a shop, you can then put items in it. Set the price of the items and how many you wish to sell. You can keep on doing this and add or remove items until you are happy. Remember that if you want to change the prices, you will need to remove all items of that type and put them back in with a new price. Give your shop a name and maybe a description and you will be open for business. Your shop will be selling items whether or not you are in the area or even if you are online or not. Every time an item is sold, you receive a message telling you who bought it, how many they bought and for how much gold. This gold is then stored in the shop. It remains here until you go there to collect it. Remember that if you do not collect it regularly, it may get stolen (see <a href="http://www.syrnia.com/index.php?page=manual&amp;m=thieving">Thieving</a>).<br />
<br />
<h4> Trading Posts </h4>
<br />
Trading items between players can be done at any of the trading posts located around the islands. Once at the city, click trading post in the city menu. Once the screen refreshes you will see a list of player names in the centre of the screen. Click trade next to the player's name you have agreed to trade with and once they accept the trade request you will be taken to the trading screen. Add the items and the quantity of each you wish to trade and once all the agreed upon items are displayed click accept, located above the items lists. (Should you click accept before the other player is ready, they will have the option to &quot;change trade&quot; to add or remove items or to &quot;accept&quot;) Once the screen refreshes you will see an overview of the items being traded and will be given one last chance to cancel the trade before it is completed. Click accept one last time and wait for the &quot;Trade completed successfully&quot; and you're done. <br />
